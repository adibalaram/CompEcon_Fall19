'''
This module provides two functions.
sum_calc(a,b) computes the sum of two real valued arguments
prod_cal(a,b) computes the product of two real valued arguments
'''

def sum_calc(a,b):
    '''
    This function computes the sum of two real valued arguments
    
    Args:
    a,b (Scalars): Two real values
    
    '''
    return (a+b)

def prod_calc(a,b):
    '''
    This function computes the product of two real valued arguments
    
    Args:
    a,b (Scalars): Two real values
    
    '''
    return (a*b)

